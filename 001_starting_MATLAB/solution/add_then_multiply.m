function result = add_then_multiply(a1, a2, m)
%ADD_THEN_MULTIPLY add the two numbers a1 and a2, then multiply the result
% of the addition by m.
%
% result = add_then_multiply(a1, a2, m);
%
% Inputs:
%  a1, a2  the two numbers to be added
%  m  the number to multiply the sum by
% Outputs:
%  r  the result of addition and then multiplication

result = (a1+a2) * m;

end

